/*
    There are two types of data types in C#:
    1. Value types
    2. Reference types
    -------
    Value types:    (allocated on stack)
        bool, byte, char, decimal, double, enum, float, int, long, sbyte, short, struct, uint, ulong, ushort
        add ? to make it nullable

    Reference types:    (allocated on heap)
        class, record, delegate, array, or interface

    * both reference and value types are derived from the ultimate base class Object. 

*/